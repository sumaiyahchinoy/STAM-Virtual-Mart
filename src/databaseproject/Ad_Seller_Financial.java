    public void UpdateTable() {

        try {
            String Str_Query = "SELECT ordert.order_id, itemt.item_id, item.item_name, itemt.quantity, itemt.line_total, ordert.order_date \n"
                    + "FROM order_transaction ordert JOIN item_transaction itemt\n"
                    + "ON ordert.order_id = itemt.order_id\n"
                    + "JOIN item_detail item\n"
                    + "ON itemt.item_id = item.item_id\n"
                    + "WHERE item.Seller_id = ?;";
            PreparedStatement ps = mycon.prepareStatement(Str_Query);
            ps.setInt(1, Seller_Id);

            ResultSet resultSet = ps.executeQuery();
            java.sql.Date sqlDate;
            int i = 0;
            String TotalEarned = "";
            while (resultSet.next()) {
                String Order_Id = String.valueOf(resultSet.getInt("Ordert.Order_ID"));
                String Item_Id = String.valueOf(resultSet.getInt("Itemt.Item_ID"));
                String Item_Name = resultSet.getString("Item.Item_NAME");
                String Quantity = String.valueOf(resultSet.getInt("Itemt.Quantity"));
                String LineTotal = String.valueOf(resultSet.getInt("Itemt.Line_total"));
                //String Cost = "0";
                String Cost = String.valueOf(Integer.parseInt(LineTotal)/Integer.parseInt(Quantity));
                sqlDate = resultSet.getDate("Ordert.order_date");
                Object row[] = {Order_Id, Item_Id, Item_Name, Quantity, Cost, LineTotal, sqlDate};
                //System.out.println(row.toString());
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.insertRow(i, row);
                i++;
            }

            String Str_Query2 = "SELECT TOTAL_EARNED FROM SELLER WHERE Seller_id=?";
            PreparedStatement ps2 = mycon.prepareStatement(Str_Query2);
            ps2.setInt(1, Seller_Id);

            ResultSet resultSet2 = ps2.executeQuery();
            if (resultSet2.next()) {
                TotalEarned = String.valueOf(resultSet2.getInt("TOTAL_EARNED"));
            }
            total.setText(TotalEarned);
            total.setEditable(false);

        } catch (SQLException ex) {
            Logger.getLogger(Ad_ActiveSeller.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

